n = int(input('Enter a number: '))
if n == 1:
    print(f'{n} is ONE')
else:
    if n == 2:
        print(f'{n} is TWO')
    else:
        if n == 3:
            print(f'{n} is Three')
        else:
            if n == 4:
                print(f'{n} is FOUR')
            else:
                if n == 5:
                    print(f'{n} is FIVE')
                else:
                    if n == 6:
                        print(f'{n} is SIX')
                    else:
                        if n == 7:
                            print(f'{n} is SEVEN')
                        else:
                            if n == 8:
                                print(f'{n} is EIGHT')
                            else:
                                if n == 9:
                                    print(f'{n} is NINE')
                                else:
                                    print(f'{n} is NUMBER')
